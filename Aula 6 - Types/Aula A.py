'''
Iniciar com letras, pode conter números, separar _, letras minúsculas
'''
nome = 'Everton'
idade = 22
altura = 1.80
e_maior = idade > 18
print('Nome =', nome, type(nome))  # Pode se ver a class que está dentro da variavel
print('Idade =', idade, type(idade))
print('Altura =', altura, type(altura))
print('É maior de 18 =', e_maior, type(e_maior))
print('Idade vezes Altura = ', idade * altura)  # Pode usar operadores nas variaveis
