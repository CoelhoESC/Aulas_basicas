'''
Operadores Aritmeticos
+
-
*
/
//
**
%
()
'''
print('Multiplicação *', 10 * 10)
print('Multiplicação *', '10' * 10)  # Pode ser feito uma repetição de str
print('Adição +', 10 + 10)
print('Adição +', '10' + '10')  # Pode ser feito uma concatenação de str/valores
print('Adição +', 'Everton' + ' ' + 'Coelho' + ' ' + 'e ele tem ' + str(22) + ' anos')  # Pode converte tipos
print('Subtração -', 10 - 4)
print('Divisão /', 10 / 2)  # Ira dar um valor ponto flutuante
print('Divisão interia //', 10.5 // 3.5, 'ou', 10 // 3)  # Ira arredondar o valor
print('Potencia **', 2 ** 10)  # base / expoente
print('Raiz quadrada por meio: 81 = ', 81 ** (1/2))
print(f'Raiz cubicá: 127 = {127 ** (1/3):.2f}')
print('Resto da divisão %', 10 % 5, 'ou', 10 % 3)  # Está fazendo a divisão mas ira obler o resto!
