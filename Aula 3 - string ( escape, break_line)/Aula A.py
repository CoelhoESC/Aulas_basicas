"""
str - string
"""
print('Alguma coisa')
print("Outra coisa")
# str é tudo que esta dentro de aspa
print(123456)  # Outro tipo primitivo
print("Essa é 'string' - (str)")  # Pode se usar aspas simples e duplas juntas
print('Esse é meu \'digite qualquer coisa\' (str).')  # Usando \ para ser escape
print('Esse é meu \n texto (str).')  # Usando \n pode quebrar a linha
print(r'Esse é meu \n texto (str).')  # Usando 'r' antes da str, não há quebra de linha
